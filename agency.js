function change1(){
    
    let child1 = document.getElementById('child1');
    let circle1 = document.getElementById('circle1');
    
    
    child1.addEventListener('mouseover',function(){
    
    child1.style.backgroundColor = 'white';
    circle1.style.backgroundColor = '#ff1481';
    
})


child1.addEventListener('mouseout',function(){
    
    child1.style.backgroundColor = '#2e4158';
    circle1.style.backgroundColor = '#162b45';
    
})
}change1();

